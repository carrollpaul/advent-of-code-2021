def lanternfish(pop: list[int], cycles: int = 80) -> int:
    for _ in range(cycles):
        new_fish = 0
        for i, val in enumerate(pop):
            if val == 0:
                new_fish += 1
                pop[i] = 6
            else:
                pop[i] -= 1

        if new_fish:
            for _ in range(new_fish):
                pop.append(8)

    print(len(pop))


